package engine.Config;

import engine.Model.QuizUser;
import org.springframework.security.core.GrantedAuthority;
import org.springframework.security.core.userdetails.UserDetails;

import java.util.Collection;
import java.util.List;

public class QuizUserDetails implements UserDetails {

    private final QuizUser quizUser;

    public QuizUserDetails(QuizUser quizUser) {
        this.quizUser = quizUser;
    }

    @Override
    public Collection<? extends GrantedAuthority> getAuthorities() {
        return List.of();
    }

    @Override
    public String getPassword() {
        return this.quizUser.getPassword();
    }

    @Override
    public String getUsername() {
        return this.quizUser.getEmail();
    }

    @Override
    public boolean isAccountNonExpired() {
        return true;
    }

    @Override
    public boolean isAccountNonLocked() {
        return true;
    }

    @Override
    public boolean isCredentialsNonExpired() {
        return true;
    }

    @Override
    public boolean isEnabled() {
        return true;
    }
}
